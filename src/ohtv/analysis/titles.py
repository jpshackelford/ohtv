@dataclass
class TitleGenerationResult:
    """Result of a batched title-generation pass.

    Attributes:
        titles: Mapping of ``conversation_id -> generated_title``.
            Only ids the LLM actually returned a usable title for.
        missing_ids: Ids that were requested but never received a
            title (LLM omitted them and the single-conv retry also
            failed). Empty in the common case.
        cost: Accumulated LLM cost across all chunk + retry calls.
        chunks_called: Number of LLM calls actually made (batched
            chunks plus any single-conv retries).
    """

    titles: dict[str, str] = field(default_factory=dict)
    missing_ids: list[str] = field(default_factory=list)
    cost: float = 0.0
    chunks_called: int = 0


# Type alias for an LLM completion callable. Tests inject a stub here
# to avoid any dependency on the OpenHands SDK / live network.
#
# The callable takes (system_prompt, user_prompt) and returns
# (response_text, cost_dollars).
LLMCallable = Callable[[str, str], tuple[str, float]]


def _default_llm_callable(model: str | None) -> LLMCallable:
    """Build an LLM callable backed by ``openhands.sdk.LLM``.

    Imported lazily so the rest of the module stays import-cheap and
    tests can stub the call site without dragging the SDK in.
    """
    os.environ.setdefault("OPENHANDS_SUPPRESS_BANNER", "1")

    from openhands.sdk import LLM, Message, TextContent

    base = LLM.load_from_env()
    if model:
        llm = LLM(model=model, api_key=base.api_key, base_url=base.base_url)
    else:
        llm = base

    def _call(system_prompt: str, user_prompt: str) -> tuple[str, float]:
        messages = [
            Message(role="system", content=[TextContent(type="text", text=system_prompt)]),
            Message(role="user", content=[TextContent(type="text", text=user_prompt)]),
        ]
        response = llm.completion(messages)
        text = ""
        for item in response.message.content:
            if hasattr(item, "text"):
                text += item.text
        cost = response.metrics.accumulated_cost
        return text, cost

    return _call


def _chunk(items: list, size: int):
    """Yield successive ``size``-sized chunks of ``items``."""
    if size <= 0:
        raise ValueError(f"batch_size must be > 0, got {size}")
    for i in range(0, len(items), size):
        yield items[i : i + size]


def _format_user_prompt(items: list[tuple[str, str]]) -> str:
    """Wrap ``items`` as the JSON-in/JSON-out user message."""
    payload = [{"id": conv_id, "description": desc} for conv_id, desc in items]
    return (
        "Convert the following conversation descriptions into titles "
        "following the rules above. Respond with JSON only.\n\n"
        f"{json.dumps(payload, ensure_ascii=False, indent=2)}"
    )


def _enforce_length(
    raw_titles: dict[str, str],
    items: list[tuple[str, str]],
    llm_call: LLMCallable,
    system_prompt: str,
) -> tuple[dict[str, str], float, int]:
    """Re-prompt single-conv for any title exceeding the hard length cap.

    Falls back to a blunt truncate-with-ellipsis when the model still
    cannot produce a short-enough title — we never want a batch run to
    fail just because one description is verbose.

    Returns:
        ``(final_titles, extra_cost, extra_chunk_calls)``.
    """
    item_by_id = dict(items)
    final = dict(raw_titles)
    extra_cost = 0.0
    extra_calls = 0

    for conv_id, title in list(raw_titles.items()):
        # Tolerate small overshoots without burning another LLM call.
        if len(title) <= MAX_TITLE_CHARS:
            continue
        if len(title) <= MAX_TITLE_CHARS + _TRUNCATE_OVERSHOOT:
            final[conv_id] = _truncate_title(title)
            log.debug("Truncated overlong title for %s (%d -> %d chars)",
                      conv_id, len(title), len(final[conv_id]))
            continue

        desc = item_by_id.get(conv_id, "")
        log.debug("Re-prompting %s for shorter title (%d chars)", conv_id, len(title))
        single_prompt = _format_user_prompt([(conv_id, desc)])
        extra_calls += 1
        try:
            retry_text, retry_cost = llm_call(system_prompt, single_prompt)
        except Exception as e:
            log.warning("Length-retry LLM call failed for %s: %s", conv_id, e)
            final[conv_id] = _truncate_title(title)
            continue
        extra_cost += retry_cost
        try:
            retry_parsed = parse_titles_response(retry_text)
        except ValueError:
            retry_parsed = {}
        retried_title = retry_parsed.get(conv_id, "").strip()
        if retried_title and _is_acceptable(retried_title):
            final[conv_id] = retried_title
        else:
            # Last resort: hard truncate the original.
            final[conv_id] = _truncate_title(title)
    return final, extra_cost, extra_calls


def generate_titles_batch(
    items: list[tuple[str, str]],
    *,
    model: str | None = None,
    batch_size: int = DEFAULT_BATCH_SIZE,
    llm_call: LLMCallable | None = None,
    system_prompt: str | None = None,
) -> TitleGenerationResult:
    """Generate titles for a list of (conversation_id, description) pairs.

    The items are split into chunks of ``batch_size``; each chunk is
    sent to the LLM as one JSON-array round-trip. If a chunk fails to
    parse cleanly (malformed JSON, wrong shape), each conversation in
    that chunk is retried as a single-item call before being declared
    missing.

    Args:
        items: List of ``(conversation_id, description)`` pairs.
            Descriptions may be empty; the prompt tells the LLM to
            fall back to a generic title in that case.
        model: Optional model override (e.g. ``"haiku"``). When ``None``,
            falls back to ``LLM_MODEL`` from the environment.
        batch_size: Number of items per LLM call. Default
            ``DEFAULT_BATCH_SIZE``.
        llm_call: Optional pre-built LLM callable. Mainly for tests —
            production code passes ``None`` and lets this module build
            one from the environment.
        system_prompt: Optional system-prompt override. When ``None``,
            loads ``prompts/titles/default.md`` (user override at
            ``~/.ohtv/prompts/titles/default.md``).

    Returns:
        A :class:`TitleGenerationResult`.
    """
    if not items:
        return TitleGenerationResult()

    if llm_call is None:
        llm_call = _default_llm_callable(model)
    if system_prompt is None:
        system_prompt = _load_titles_prompt()

    result = TitleGenerationResult()
    item_by_id: dict[str, str] = dict(items)

    for chunk in _chunk(items, batch_size):
        chunk_ids = [conv_id for conv_id, _ in chunk]
        user_prompt = _format_user_prompt(chunk)
        result.chunks_called += 1
        try:
            response_text, cost = llm_call(system_prompt, user_prompt)
        except Exception as e:
            log.warning("LLM call failed for chunk of %d items: %s", len(chunk), e)
            result.missing_ids.extend(chunk_ids)
            continue
        result.cost += cost

        try:
            parsed = parse_titles_response(response_text)
        except ValueError as e:
            log.info(
                "Batch chunk of %d failed to parse (%s); retrying single-conv",
                len(chunk), e,
            )
            parsed = {}

        # Determine which chunk ids were not satisfied by the batch response
        unresolved = [conv_id for conv_id in chunk_ids if conv_id not in parsed]
        result.titles.update({k: v for k, v in parsed.items() if k in item_by_id})

        # Retry each unresolved id one at a time
        for conv_id in unresolved:
            desc = item_by_id.get(conv_id, "")
            single_prompt = _format_user_prompt([(conv_id, desc)])
            result.chunks_called += 1
            try:
                retry_text, retry_cost = llm_call(system_prompt, single_prompt)
            except Exception as e:
                log.warning("Single-conv retry failed for %s: %s", conv_id, e)
                result.missing_ids.append(conv_id)
                continue
            result.cost += retry_cost
            try:
                retry_parsed = parse_titles_response(retry_text)
            except ValueError:
                retry_parsed = {}
            if conv_id in retry_parsed:
                result.titles[conv_id] = retry_parsed[conv_id]
            else:
                result.missing_ids.append(conv_id)

    # Post-validation: enforce hard length cap on any titles the LLM
    # returned. Single-conv length retries happen inside ``_enforce_length``.
    if result.titles:
        adjusted, extra_cost, extra_calls = _enforce_length(
            result.titles, items, llm_call, system_prompt,
        )
        result.titles = adjusted
        result.cost += extra_cost
        result.chunks_called += extra_calls

    return result


