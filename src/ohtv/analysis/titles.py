"""Generate concise titles for cloud conversations from cached gen-objs analyses.

The :func:`generate_titles_batch` function is the main entry point — it
chunks a list of ``(conv_id, description)`` items, sends each chunk to
the LLM as a single batch JSON-in / JSON-out call, and falls back to
single-conversation retries when a chunk fails to parse cleanly.

The :func:`description_from_analysis` helper extracts a uniform
"description blob" from any cached :class:`ObjectiveAnalysis` variant
(brief / standard / detailed / *_assess). This is what the LLM sees as
input.

This module is intentionally light-weight — it does not touch the cloud
API, the local DB, or the manifest. Callers handle the apply phase
(see ``ohtv gen titles`` in ``cli.py``).
"""

from __future__ import annotations

import json
import logging
import os
import re
from dataclasses import dataclass, field
from pathlib import Path
from typing import Callable

from ohtv.analysis.objectives import ObjectiveAnalysis

log = logging.getLogger("ohtv")


# =============================================================================
# Constants
# =============================================================================


#: Default LLM-side chunk size — convs per batched JSON-in/JSON-out call.
#: Empirically a good token / accuracy balance: brief descriptions average
#: ~30 tokens each, so 25 fits comfortably inside a 4k input window.
DEFAULT_BATCH_SIZE = 25

#: Maximum title length we accept from the LLM (chars, including a
#: leading emoji and spaces). Enforced post-LLM; offenders trigger a
#: single-conv re-prompt, then a hard truncate fallback.
MAX_TITLE_CHARS = 50

#: Synthesis schema version for title generation (Issue #191).
#: Bump this whenever the prompt or title generation logic changes
#: to invalidate stale cache entries.
SYNTHESIS_SCHEMA_VERSION = "v1"


#: Placeholder titles auto-generated by OpenHands Cloud for unrenamed
#: conversations. The id portion is hex; we accept 5–32 chars to cover
#: both the short 5-char prefix display and the full 32-char id form.
#:
#: Empirical match rate (Issue #89): 13 of 20 most recent unrenamed
#: cloud conversations.
_PLACEHOLDER_TITLE_RE = re.compile(r"^Conversation [0-9a-f]{5,32}$")


# =============================================================================
# Selector predicate
# =============================================================================


def is_placeholder_title(title: str | None) -> bool:
    """Return True when ``title`` is missing or auto-generated.

    The predicate matches:

    * ``None``
    * empty / whitespace-only strings
    * Strings matching ``^Conversation [0-9a-f]{5,32}$`` exactly

    Real titles — even short ones — never match. Trailing or leading
    whitespace around the placeholder pattern is **not** considered a
    placeholder; the cloud API stores the title verbatim and any
    intentional whitespace is treated as user intent.

    Args:
        title: Title string from ``ConversationInfo.title`` or
            ``manifest_entry["title"]``.

    Returns:
        True iff the title is missing or matches the placeholder regex.
    """
    if title is None:
        return True
    if not title.strip():
        return True
    return _PLACEHOLDER_TITLE_RE.match(title) is not None


# =============================================================================
# Description extraction
# =============================================================================


def description_from_analysis(analysis: ObjectiveAnalysis) -> str:
    """Extract a uniform "description blob" from any analysis variant.

    All variants are mapped onto a single short string suitable for the
    LLM titling prompt:

    * **brief** / **brief_assess** — just ``goal``
    * **standard** / **standard_assess** — ``goal`` + each
      ``primary_outcomes`` joined with ``"; "``
    * **detailed** / **detailed_assess** — ``goal`` (if present) +
      ``primary_objectives[*].description`` joined with ``"; "``.
      Sub-objectives are deliberately ignored to keep the LLM prompt
      compact.

    If the analysis carries no meaningful text at all (e.g. only an
    empty ``primary_objectives`` list), the returned string is the
    empty string — callers should treat empty descriptions as a
    cache-miss for titling purposes.

    Args:
        analysis: A cached :class:`ObjectiveAnalysis`.

    Returns:
        A short description string. May be empty if the analysis has
        no usable content.
    """
    detail = analysis.detail_level

    if detail in ("brief", "brief_assess"):
        return (analysis.goal or "").strip()

    if detail in ("standard", "standard_assess"):
        parts: list[str] = []
        if analysis.goal:
            parts.append(analysis.goal.strip())
        for outcome in analysis.primary_outcomes or []:
            if outcome and outcome.strip():
                parts.append(outcome.strip())
        return "; ".join(parts)

    # detailed / detailed_assess (or anything we don't recognise)
    parts = []
    if analysis.goal:
        parts.append(analysis.goal.strip())
    if analysis.summary:
        parts.append(analysis.summary.strip())
    for obj in analysis.primary_objectives or []:
        if obj.description and obj.description.strip():
            parts.append(obj.description.strip())
    return "; ".join(parts)


# =============================================================================
# LLM response parsing
# =============================================================================


def parse_titles_response(response_text: str) -> dict[str, str]:
    """Parse a JSON-array LLM response into a ``{id: title}`` dict.

    The expected response shape is::

        [{"id": "...", "title": "..."}, ...]

    Markdown code fences are tolerated and stripped. Each entry must
    be a dict with non-empty ``id`` and ``title`` string fields;
    malformed entries are silently skipped (the caller decides what to
    do about missing ids).

    Args:
        response_text: Raw text from the LLM completion.

    Returns:
        Mapping ``conversation_id -> title``. Empty if nothing parses.

    Raises:
        ValueError: If the outer JSON does not parse or is not a list.
    """
    text = response_text.strip()

    if text.startswith("```"):
        lines = text.split("\n")
        lines = lines[1:]
        if lines and lines[-1].strip() == "```":
            lines = lines[:-1]
        text = "\n".join(lines).strip()

    try:
        parsed = json.loads(text)
    except json.JSONDecodeError as e:
        raise ValueError(f"LLM response is not valid JSON: {e}") from e

    if not isinstance(parsed, list):
        raise ValueError(
            f"Expected JSON array, got {type(parsed).__name__}: {text[:120]!r}"
        )

    out: dict[str, str] = {}
    for entry in parsed:
        if not isinstance(entry, dict):
            continue
        conv_id = entry.get("id")
        title = entry.get("title")
        if not isinstance(conv_id, str) or not isinstance(title, str):
            continue
        conv_id = conv_id.strip()
        title = title.strip()
        if not conv_id or not title:
            continue
        out[conv_id] = title
    return out


# =============================================================================
# Title post-validation
# =============================================================================


# Tolerance: we accept the model returning a title slightly longer than
# the hard cap (it happens) and truncate without re-prompting up to a
# small overshoot. Past this we re-prompt single-conv to give it a
# chance to do better than a blunt truncation.
_TRUNCATE_OVERSHOOT = 8


def _truncate_title(title: str, limit: int = MAX_TITLE_CHARS) -> str:
    """Hard-truncate a title to ``limit`` characters with an ellipsis.

    The ellipsis (``…``, U+2026) costs 1 character against ``limit``,
    matching how the cloud UI renders truncations.
    """
    if len(title) <= limit:
        return title
    # 1 char for the ellipsis
    return title[: limit - 1].rstrip() + "…"


def _is_acceptable(title: str) -> bool:
    """Return True iff ``title`` already meets the hard length cap."""
    return bool(title) and len(title) <= MAX_TITLE_CHARS


