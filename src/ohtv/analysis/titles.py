@dataclass
class CachedTitleResult:
    """Result of cached title generation.
    
    Attributes:
        titles: Mapping of ``conversation_id -> title``.
        was_cached: Mapping of ``conversation_id -> bool`` indicating
            whether the title was from cache (True) or freshly synthesized (False).
        cost: Accumulated LLM cost for titles that needed synthesis.
        cache_hits: Number of titles served from cache.
        cache_misses: Number of titles that needed synthesis.
        missing_ids: Ids that were requested but never received a title
            (LLM omitted them). Empty in the common case.
    """
    titles: dict[str, str] = field(default_factory=dict)
    was_cached: dict[str, bool] = field(default_factory=dict)
    cost: float = 0.0
    cache_hits: int = 0
    cache_misses: int = 0
    missing_ids: list[str] = field(default_factory=list)


def generate_titles_with_cache(
    conversations: list[tuple[str, str, str]],
    *,
    model: str | None = None,
    force_refresh: bool = False,
    batch_size: int = DEFAULT_BATCH_SIZE,
    llm_call: LLMCallable | None = None,
) -> CachedTitleResult:
    """Generate titles using cache when available.
    
    This function wraps :func:`generate_titles_batch` with synthesis cache
    integration (Issue #191). For each conversation, it checks the
    ``conversation_synthesis`` table for a valid cached title. Only uncached
    conversations are sent to the LLM. Results are automatically stored in
    the cache for future calls.
    
    Cache validation logic:
    
    * Cache hit requires matching ``conversation_updated_at``,
      ``synthesis_version``, and ``synthesis_model``
    * ``--force`` flag bypasses cache entirely
    * New titles are automatically cached with current metadata
    
    Args:
        conversations: List of ``(conversation_id, updated_at, description)``
            tuples. The ``updated_at`` must be from ``conversations.updated_at``
            (ISO 8601 string). The ``description`` should be from
            :func:`description_from_analysis` or equivalent.
        model: Optional model override (e.g. ``"haiku"``). When ``None``,
            falls back to ``LLM_MODEL`` from the environment.
        force_refresh: If True, bypass cache and force re-synthesis.
        batch_size: Number of items per LLM call. Default
            ``DEFAULT_BATCH_SIZE``.
        llm_call: Optional pre-built LLM callable. Mainly for tests —
            production code passes ``None`` and lets the function build
            one from the environment.
    
    Returns:
        A :class:`CachedTitleResult` with titles, cache statistics, and cost.
    
    Examples:
        >>> # Basic usage with cache
        >>> conversations = [
        ...     ("abc123", "2024-01-01T00:00:00Z", "Fix auth bug"),
        ...     ("def456", "2024-01-02T00:00:00Z", "Add new feature"),
        ... ]
        >>> result = generate_titles_with_cache(conversations)
        >>> print(f"Cache hit rate: {result.cache_hits}/{len(conversations)}")
        >>> for conv_id, title in result.titles.items():
        ...     cached = " (cached)" if result.was_cached[conv_id] else ""
        ...     print(f"{conv_id}: {title}{cached}")
    """
    from ohtv.db import get_ready_connection
    from ohtv.db.stores.synthesis_cache_store import SynthesisCacheStore
    
    # Determine effective model name for cache key
    if model:
        effective_model = model
    else:
        effective_model = os.environ.get("LLM_MODEL", "openai/gpt-4o-mini")
    
    result = CachedTitleResult()
    
    with get_ready_connection() as conn:
        store = SynthesisCacheStore(conn)
        
        needs_synthesis: list[tuple[str, str, str]] = []
        
        # Check cache for each conversation
        for conv_id, updated_at, description in conversations:
            if not force_refresh:
                cached_title = store.get_cached_title(
                    conv_id, updated_at, effective_model, SYNTHESIS_SCHEMA_VERSION
                )
                if cached_title:
                    result.titles[conv_id] = cached_title
                    result.was_cached[conv_id] = True
                    result.cache_hits += 1
                    log.debug("Cache hit for %s", conv_id)
                    continue
            
            # Cache miss or force refresh
            needs_synthesis.append((conv_id, updated_at, description))
            result.cache_misses += 1
        
        # Synthesize uncached titles in batch
        if needs_synthesis:
            log.info("Synthesizing %d titles (cache misses)", len(needs_synthesis))
            
            # Extract (id, description) for the batch call
            batch_input = [(cid, desc) for cid, _, desc in needs_synthesis]
            
            gen_result = generate_titles_batch(
                batch_input,
                model=model,
                batch_size=batch_size,
                llm_call=llm_call,
            )
            
            result.cost = gen_result.cost
            result.missing_ids = gen_result.missing_ids
            
            # Cache new results and add to output
            for conv_id, updated_at, _ in needs_synthesis:
                if conv_id in gen_result.titles:
                    title = gen_result.titles[conv_id]
                    result.titles[conv_id] = title
                    result.was_cached[conv_id] = False
                    
                    # Estimate tokens (rough approximation: 1 token ≈ 4 chars)
                    tokens_used = len(title) // 4
                    
                    # Store in cache
                    store.upsert_title(
                        conv_id, updated_at, title, effective_model,
                        SYNTHESIS_SCHEMA_VERSION, tokens_used
                    )
                    log.debug("Cached new title for %s", conv_id)
            
            conn.commit()
    
    return result


