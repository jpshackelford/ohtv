# Prompt loading
# =============================================================================


def _load_titles_prompt() -> str:
    """Load the titles prompt body, preferring the user override.

    Search order:

    1. ``~/.ohtv/prompts/titles/default.md`` (or ``OHTV_DIR`` override)
    2. Bundled ``src/ohtv/prompts/titles/default.md``

    Returns the prompt body with YAML frontmatter stripped.

    Raises:
        FileNotFoundError: If neither location has the prompt file.
    """
    from ohtv.prompts.discovery import resolve_prompt

    try:
        meta = resolve_prompt("titles", "default")
        return meta.content
    except ValueError:
        # Fall back to direct file read (e.g. discovery cache cleared
        # between tests, or a malformed user override).
        from ohtv.config import get_ohtv_dir

        user_path = get_ohtv_dir() / "prompts" / "titles" / "default.md"
        if user_path.exists():
            text = user_path.read_text()
        else:
            bundled = Path(__file__).parent.parent / "prompts" / "titles" / "default.md"
            text = bundled.read_text()
        # Strip frontmatter if present
        if text.startswith("---\n"):
            parts = text.split("---\n", 2)
            if len(parts) >= 3:
                return parts[2]
        return text


def load_titles_prompt_hash() -> str:
    """Return the 16-hex content hash of the active titles prompt."""
    import hashlib

    return hashlib.sha256(_load_titles_prompt().encode()).hexdigest()[:16]


